    public static function Convert($price,$first_currency,$second_currency){
        if ($first_currency==$second_currency){
            return $price;
        };

        if ($second_currency->id == '980'){
            $price = $price*$first_currency->rate;
            return $price;
        };
        if ($first_currency->id=='980'){
            $price = $price/$second_currency->rate;
            return $price;
        };
        $price = $price*$first_currency->rate;
        $price = $price/$second_currency->rate;

        return $price;
    }
