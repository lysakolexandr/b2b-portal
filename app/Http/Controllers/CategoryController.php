    public function addPictures(Request $request)
    {
        $post_arr = $request->request->all();
        $f_string = $post_arr['f'];
        $f = json_decode($f_string);
        $catalog_id = $f->catalog_id;
        $exist = Storage::disk('local')->has('/public/img/categories');
        if($exist){
            //Storage::deleteDirectory('/public/img/categories');
        }
        foreach($f->pictures as $pic){
            $imageName = '/public/img/categories/'.$pic->name;
            $image = str_replace(' ', '+', $pic->data);
            Storage::disk('local')->put($imageName, base64_decode($image));
        }

        return true;
    }

